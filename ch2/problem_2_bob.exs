# Bob has traveled 200km in four hours
# using variables, print message showing his travel distance, time and average velocity

distance_in_km = 200
time_in_hours = 4
average_velocity = distance_in_km / time_in_hours

IO.puts "travel distance: #{distance_in_km} Km"
IO.puts "time : #{time_in_hours} hours"
IO.puts "average velocity: #{average_velocity} Km/Hours"

